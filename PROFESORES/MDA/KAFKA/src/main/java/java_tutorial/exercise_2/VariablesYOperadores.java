package java_tutorial.exercise_2;

/**
 * Programa que declara variables para almacenar un número entero y uno de tipo decimal.
 * Luego, realiza operaciones básicas como suma, resta.
 *
 * Amplía este programa implementando la multiplicación y división
 */
public class VariablesYOperadores {
    public static void main(String[] args) {
        int numeroEntero = 5;
        float numeroDecimal = 3.14f;

        System.out.println("Suma: " + (numeroEntero + numeroDecimal));
        System.out.println("Resta: " + (numeroEntero - numeroDecimal));

    }
}

