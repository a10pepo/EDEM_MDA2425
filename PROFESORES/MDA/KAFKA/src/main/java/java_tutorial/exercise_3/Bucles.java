/**
 * Programa que utilice un bucle para imprimir los números del 1 al 10 en la consola.
 *
 * Modifica el programa para hacer la tabla de la multiplicación del 8 x 7
 */
public class Bucles {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}

